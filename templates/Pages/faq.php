<div class="p-3 my-3 bg-white rounded-lg">
<h2>What happens when I follow a pathway?</h2> 
<ul>
<li>The pathway will show that you are following it, and will appear on your profile page. The rest is up to you—engage as much or as little as you see fit with the resources within each step. 
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>How do I complete a pathway?</h2> 
<ul>
<li>Once you claim all the required activities in all steps along the pathway, your pathway will be marked complete. 
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>What are the four different formats?</h2> 
<ul>
<li>Items are categorized as watch (video), listen (audio recordings, including podcasts), read (readings of all kinds, including online articles and book recommendations), and participate (including face to face and online classes you take with others, or self-reflective activities or action items that require some output from you). Using the tags, you can quickly scan for the type of activity you have the capacity to engage with right now. 
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>How do I register for a course in the Learning Curator?</h2> 
<ul>
<li>When a course offered by the Learning Centre is linked in a pathway on the Learning Curator, it will take you to the course registration page. Just click “Participate.” When you’ve completed your course, be sure to click “Claim” to show you’re finished and can see the progress on your completion wheel for that pathway. If the pathway recommends a course you’ve already taken, just click “Claim.”  
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>How do I get a book that’s recommended?</h2> 
<ul>
<li>When a book is recommended, and we hold the title in the Learning Centre Library, we will mention that in the resource record. Otherwise, you can look for the title in your public library, or in some cases in an academic library if you have alumni or community borrowing privileges.  
<li>Recommended reads will never be required to complete a step, so you do not need to purchase a book if you don’t want to. 
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>What happens when I’ve already completed a pathway, but new items are added to it later?</h2>  
<ul>
<li>The date and time you completed the pathway will show on your profile page. You will always have access to your pathway, and it will always be recorded as completed. But you can still return as you wish to engage with any new steps or resources as they are added. 
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>What are “archived” items in a pathway?</h2> 
<ul>
<li>Curators will always be keeping the pathways up-to-date. Occasionally an item may be removed from the recommendations. If an item that you claimed is no longer part of the active pathway, you can check a list of archived items to access it. 
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>Who are the learning curators?</h2> 
<ul>
<li>Curators are BC Public Service employees with a specialty in the pathway’s topic area who have volunteered to develop pathway learning for the Learning Centre 
The curators who have worked so far on the Leadership resources
<ul>
<li>run programs for Managing in the BC Public Service 
<li>develop leadership training for their ministry 
<li>work in special strategic human resources development projects 
</ul>
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>How did the curators choose the resources to include?</h2> 
<ul>
<li>The curators considered the surveyed needs of the BC public service in the area of leadership development, and chose the pathway goals based on the strongest priorities expressed by respondents. 
<li>They worked in tandem with the Corporate Leadership Development Framework staff to ensure alignment with CLDF priorities 
<li>They researched current informal learning available that would support the priorities, using a set criteria that include standards of quality and relevance for BC public service.
 </ul>
 </div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>Will the Learning Curator remember what I’ve done?</h2> 
<ul>
<li>Once you log into the Learning Curator with your IDIR for the first time, a profile is created for you. Learning Curator will retain your chosen pathways and claimed items between visits.  
<li>The Learning Curator only records your activities through the “Follow this Pathway” and “Claim” buttons.  
<li>Record of your progress stays in the Learning Curator, and is only visible to site administrators.  
</ul>
</div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>How can I share my learning with my manager?</h2> 
<ul>
<li>You can save a copy of your profile page, which will show which steps you’ve taken on which pathways. Share your saved page with your manager in a one-on-one meeting and discuss your plans. 
<li>You can enter Learning Curator pathways as goals in your MyPerformance Plan. Discuss the goal with your manager, and if you like, set target dates for completing steps and pathways. 
</ul></div>
<div class="p-3 mb-3 bg-white rounded-lg"><h2>How can I track my learning? Do my completed pathways show in my Learning Record?</h2> 
<ul>
<li>Claim activities as you complete them by clicking the claim button. This record of what videos you’ve watched, readings you’ve read, and courses you’ve taken and so on will not be reported out anywhere, but is just for you. You can view a list of previously claimed items at any time when you visit Your Profile. 
</ul>
 </div>
